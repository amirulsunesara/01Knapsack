
public class Items 
{
	private int weight;
	private int value;
	Items(int Weight , int Value)
	{
		this.weight = Weight;
		this.value = Value;
		
	}
	
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}

}
